# Example of if-elif-else statement structure

# if condition1:
#     # if-block
# elif condition2:
#     # elif-block
# else:
#     # else-block

# Example usage:
x = 10

if x > 15:
    print("x is greater than 15")
elif x > 5:
    print("x is greater than 5 but not greater than 15")
else:
    print("x is 5 or less")
