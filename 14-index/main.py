# indexing = accessing elements of a sequence using [] (indexing operator) 
# [start : end : step]

credit_card = '4111-1234-5678-1234'

print(credit_card[0])

print(credit_card[2])

print(credit_card[0:4])

print(credit_card[0:4])

print(credit_card[5:9])

print(credit_card[5:])

print(credit_card[-1])

print(credit_card[::3])

print(credit_card[::-1])

